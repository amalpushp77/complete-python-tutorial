# Indexing - Position of an element

# +ve indexing in python starts from 0
# -ve indexing in python start from -1

# String
s = 'Hello World!'
print(s)

print(s[0])

# List
l = [2, 'abc', 3+4j, False, (3, 5, 7)]
print(l)

print(l[0])
print(l[4][1])

# Tuple
t = (2, 'abc', 3+4j, False, (3, 5, 7))

print(t)
print(t[3])
