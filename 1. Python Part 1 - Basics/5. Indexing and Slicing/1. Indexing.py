#Indexing - Position of element

#Forward Indexing in python starts from 0
#Backword Indexing in python start from -1

#String
s = 'Hello World!'
print(s)

print(s[-12])

#List
l = [2,'abc',3+4j,False,(3,5,7)]

print(l)

print(l[0])
print(l[4][1])

#Tuple
t = (2,'abc',3+4j,False,(3,5,7))

print(t)

print(t[3])
